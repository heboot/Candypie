package com.heboot.entity.my;

public class MyBottomMenuModel {

    private String name;

    private int icon;

    public MyBottomMenuModel(String name, int icon) {
        this.name = name;
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }
}
